package com.freelancex.data.local

import android.content.Context
import androidx.datastore.core.DataStore
import androidx.datastore.preferences.core.Preferences
import androidx.datastore.preferences.core.booleanPreferencesKey
import androidx.datastore.preferences.core.edit
import androidx.datastore.preferences.core.stringPreferencesKey
import androidx.datastore.preferences.preferencesDataStore
import dagger.hilt.android.qualifiers.ApplicationContext
import kotlinx.coroutines.flow.Flow
import kotlinx.coroutines.flow.map
import javax.inject.Inject
import javax.inject.Singleton

private val Context.dataStore: DataStore<Preferences> by preferencesDataStore(name = "freelancex_prefs")

/**
 * Preferences Manager using DataStore for persistent storage
 */
@Singleton
class PreferencesManager @Inject constructor(
    @ApplicationContext private val context: Context
) {
    private val dataStore = context.dataStore
    
    companion object {
        val KEY_AUTH_TOKEN = stringPreferencesKey("auth_token")
        val KEY_USER_ID = stringPreferencesKey("user_id")
        val KEY_USER_EMAIL = stringPreferencesKey("user_email")
        val KEY_USER_ROLE = stringPreferencesKey("user_role")
        val KEY_IS_DARK_MODE = booleanPreferencesKey("is_dark_mode")
    }
    
    /**
     * Save authentication token
     */
    suspend fun saveAuthToken(token: String) {
        dataStore.edit { preferences ->
            preferences[KEY_AUTH_TOKEN] = token
        }
    }
    
    /**
     * Get authentication token
     */
    fun getAuthToken(): Flow<String?> {
        return dataStore.data.map { preferences ->
            preferences[KEY_AUTH_TOKEN]
        }
    }
    
    /**
     * Save user data
     */
    suspend fun saveUserData(userId: String, email: String, role: String) {
        dataStore.edit { preferences ->
            preferences[KEY_USER_ID] = userId
            preferences[KEY_USER_EMAIL] = email
            preferences[KEY_USER_ROLE] = role
        }
    }
    
    /**
     * Get user ID
     */
    fun getUserId(): Flow<String?> {
        return dataStore.data.map { preferences ->
            preferences[KEY_USER_ID]
        }
    }
    
    /**
     * Save dark mode preference
     */
    suspend fun setDarkMode(enabled: Boolean) {
        dataStore.edit { preferences ->
            preferences[KEY_IS_DARK_MODE] = enabled
        }
    }
    
    /**
     * Get dark mode preference
     */
    fun isDarkMode(): Flow<Boolean> {
        return dataStore.data.map { preferences ->
            preferences[KEY_IS_DARK_MODE] ?: false
        }
    }
    
    /**
     * Clear all preferences (logout)
     */
    suspend fun clearAll() {
        dataStore.edit { preferences ->
            preferences.clear()
        }
    }
    
    /**
     * Check if user is logged in
     */
    fun isLoggedIn(): Flow<Boolean> {
        return dataStore.data.map { preferences ->
            preferences[KEY_AUTH_TOKEN] != null
        }
    }
}
